package org.abc.xyz;

public class DataTypesPractice {
	
	public static void main(String[] args) {
		//datatype refvariable = value;
		int a = 10;
		System.out.println(a);
		
		//char -> single alphabets; ' ' 
		char b = 'z';
		System.out.println("The character value = "+b);
		
		//long,append the value with l
		long c = 123456789l;
		System.out.println(c);
		
		//String, " "
		String d = "Java @ 123 is SiMplE";
		System.out.println(d);
		
		//float , f
		float e = 2.5f;
		System.out.println(e);
		
	}
}
